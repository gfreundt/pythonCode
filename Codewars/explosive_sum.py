def exp_sum(n):
    if n % 2 == 0:
        s = 1 + sum([i for i in range(1, n // 2)]) * 2 + n // 2
    else:
        s = 1 + sum([i for i in range(1, n // 2 + 1)]) * 2
    return s


for i in range(1, 20):
    print(i, exp_sum(i))
