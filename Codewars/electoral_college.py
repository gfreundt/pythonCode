def electoral_college_electors(population):
    # create multipliers up to the maximum of theoretically possible seats
    multipliers = [1 / (i * (i - 1)) ** 0.5 for i in range(2, 385)]
    # create list of absolute priorities (all states)
    priority_list = {population[s] * m: s for m in multipliers for s in population}
    # sort list in order of highest to lowest priority
    pl = sorted(priority_list.items(), key=lambda x: x[0], reverse=True)
    # take fist 385 priorities (number of House Representatives - 1 assigned to each(50) by default) and asign states in order
    slots = [i[1] for i in pl[:385]]
    # create dictionary that accumulates all state results and adds 3 (1 House Rep starting point + 2 Senate Members per state)
    result = {s: slots.count(s) + 3 for s in population}
    # add DC's 3 House Representatives
    result.update({"District of Columbia": 3})
    # sort answer by decreasing number of electoral votes (secondary key = state alphabetically)
    print({m: n for m, n in sorted(result.items(), key=lambda i: (-i[1], i[0]))})


